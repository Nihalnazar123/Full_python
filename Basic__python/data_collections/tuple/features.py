# features of tuple

# it is heterogeneous (store multiple data types in a set)
t=1,2,3,4,'hi',9.6
print(t)

# tuple keeps order
s=9,2,5,1,6,8
print(s)

# tuple support duplication
p=1,2,3,1,2,3
print(p)

# tuple support indexing and nesting
print(p[0])
c=(1,2,3,4,5,6,(9,8))
print(c)

# tuple is not mutable( updation is not possible)