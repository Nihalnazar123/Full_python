# dictionary features


# it keeps order
d={1:3,4:8,0:0}
print(d)

# it is heterogeneous (store multiple data types in a set)
d={1:'a','b':8,0:0}
print(d)


# dictionary support duplication in case of values but in case of keys it does not support duplication
d={1:1,2:1,0:0}
print(d)


# dictionary do not support indexing but we can key in place of indexing

# in dictionary  when we iterate it only takes key

# in dictionary nesting is possible
d={1:1,2:1,0:{8:2,9:5,6:{5:4}}}
print(d)

# it is mutable(updation is possible)



