# to remove a single element from a set - syntax
#                                   s.remove()
s={1,2,3,4,5}
s.remove(3)
print(s)
# to remove total elements from a set  -  syntax
#                                      s.clear()
s.clear()
print(s)

# to delete a set -  syntax
#                del s
del s
print(s)